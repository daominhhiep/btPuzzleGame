function changeImage1() {
    let img1 = document.getElementById('pic1');
    if (img1.src.match('cat')) {
        img1.src = 'image/monkey1.jpg';
        img1.value = "monkey";
    } else if (img1.src.match('monkey')) {
        img1.src = 'image/panda1.jpg';
        img1.value = "panda";
    } else if (img1.src.match('panda')) {
        img1.src = 'image/panther1.jpg';
        img1.value = "panther";
    } else {
        img1.src = 'image/cat1.jpg';
        img1.value = "cat";
    }


}
